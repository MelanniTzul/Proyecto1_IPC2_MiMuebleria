
package ObjetosMuebleria;


public class compra {
    
    private int Nit;
    private String Nombre;

    public compra() {
    }

    public compra(int Nit, String Nombre) {
        this.Nit = Nit;
        this.Nombre = Nombre;
    }

    public int getNit() {
        return Nit;
    }

    public void setNit(int Nit) {
        this.Nit = Nit;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    
    
    
}
